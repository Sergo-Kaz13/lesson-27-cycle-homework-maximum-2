// ? Виведи таблицю множення для всіх чисел від 2 до 9. Кожне число необхідно помножити на числа від 1 до 10.

let arr = [];

for (let i = 2; i < 10; i++) {
  arr[i - 2] = [];

  for (let j = 1; j < 11; j++) {
    arr[i - 2].push(i * j);
  }
}

console.log(arr);
